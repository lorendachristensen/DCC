# Helper to build text tiles via 8x8 patterns using 1-bit font
def font_tile(rows):
    """8 rows of 8 chars, '.' = 0, '#' = 1 (palette color 1 on palette 0)."""
    out_rows = []
    for row in rows:
        assert len(row) == 8, f"bad: {row!r}"
        out_rows.append("".join("1" if c == "#" else "0" for c in row))
    return tile_from_ascii(out_rows)

