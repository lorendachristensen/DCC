"""
Generate CHR-ROM for Dungeon Crawler Carl NES.
Each tile is 8x8 pixels, 2 bit planes, 16 bytes total.
Pattern table 0 = sprites, Pattern table 1 = background.
"""

def tile_from_ascii(rows):
    """Convert 8 rows of 8 chars (0-3) into 16 bytes CHR data."""
    assert len(rows) == 8, f"Tile needs 8 rows, got {len(rows)}"
    plane0 = bytearray()
    plane1 = bytearray()
    for row in rows:
        assert len(row) == 8, f"Row needs 8 chars: {row!r}"
        p0 = 0
        p1 = 0
        for i, c in enumerate(row):
            v = int(c)
            bit = 7 - i
            if v & 1:
                p0 |= (1 << bit)
            if v & 2:
                p1 |= (1 << bit)
        plane0.append(p0)
        plane1.append(p1)
    return bytes(plane0 + plane1)

