# =============================================================================
# Build output CHR
# =============================================================================
def build_pattern_table(tiles_dict):
    data = bytearray(16 * 256)  # 4KB = 256 tiles x 16 bytes
    for idx, tile in tiles_dict.items():
        if idx < 0 or idx > 255:
            continue
        offset = idx * 16
        data[offset:offset+16] = tile
    return bytes(data)

